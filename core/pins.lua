local _, ns = ...

-------------------------------------------------------------------------------
------------------------------------- PIN -------------------------------------
-------------------------------------------------------------------------------

local function Pin(attrs)
    local pin = {}
    if attrs then for k, v in pairs(attrs) do pin[k] = v end end

    if not pin.group then error('Pin must have a group!') end

    return pin
end

ns.pin = {
    Pin = Pin
}
